function extendTypescriptToWebpack(cfg) {
  // added the type-script supports
  cfg.resolve.plugins = [
    new TsconfigPathsPlugin({ configFile: TSCONFIG })
  ]
  cfg.resolve.extensions.push('.ts')
  cfg.module.rules.push({
    test: /\.ts$/,
    loader: 'ts-loader',
    options: {
      configFile: TSCONFIG,
      appendTsSuffixTo: [/\.vue$/]
    }
  })
}

